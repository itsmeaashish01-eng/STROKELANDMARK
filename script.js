const normalize = (text) => (text || '').toLowerCase().replace(/[–—_]/g, '-').replace(/[^a-z0-9+\-\s]/g, ' ').replace(/\s+/g, ' ').trim();
function trialTerms(trial) {
  const base = [trial.name, ...(trial.aliases || [])];
  const compact = trial.name.replace(/[\s\-]/g, '');
  return [...new Set([...base, compact])].filter(Boolean);
}
function scanText() {
  const raw = el('sourceText').value || '';
  const text = normalize(raw);
  const compactText = text.replace(/[\s\-]/g, '');
  detectedNames = new Set();
  if (text.length > 0) {
    trialDatabase.forEach(trial => {
      const found = trialTerms(trial).some(term => {
        const n = normalize(term);
        if (!n) return false;
        if (n.length <= 4) return text.includes(` ${n} `) || text.startsWith(`${n} `) || text.endsWith(` ${n}`);
        return text.includes(n) || compactText.includes(n.replace(/[\s\-]/g, ''));
      });
      if (found) detectedNames.add(trial.name);
    });
  }
  updateExtraction();
  renderTrials();
}
function statusFor(trial) {
  if (detectedNames.has(trial.name)) return 'found in uploaded text';
  if ((el('sourceText').value || '').trim()) return 'important trial not detected in text';
  return 'built-in landmark database';
}
function uniqueByName(items) {
  const map = new Map();
  items.forEach(t => { if (!map.has(t.name)) map.set(t.name, t); });
  return [...map.values()];
}
function updateExtraction() {
  const unique = uniqueByName(trialDatabase);
  const found = unique.filter(t => detectedNames.has(t.name));
  const missing = unique.filter(t => !detectedNames.has(t.name));
  el('totalTrials').textContent = unique.length;
  el('foundTrials').textContent = found.length;
  el('missingTrials').textContent = missing.length;
  el('foundList').className = found.length ? 'pill-list' : 'pill-list empty';
  el('foundList').innerHTML = found.length ? found.map(t => `<span class="pill found">${escapeHtml(t.name)}</span>`).join('') : 'No trials detected yet.';
  el('missingList').className = missing.length ? 'pill-list' : 'pill-list empty';
  el('missingList').innerHTML = missing.length ? missing.slice(0, 90).map(t => `<span class="pill missing">${escapeHtml(t.name)}</span>`).join('') : 'No missing trials detected.';
}
function fillSelect(id, values) {
  const select = el(id);
  const current = select.value || 'all';
  const label = select.options[0].textContent;
  select.innerHTML = `<option value="all">${label}</option>` + values.sort().map(v => `<option value="${escapeAttr(v)}">${escapeHtml(v)}</option>`).join('');
  select.value = [...select.options].some(o => o.value === current) ? current : 'all';
}
function populateFilters() {
  fillSelect('categoryFilter', [...new Set(trialDatabase.map(t => t.category))]);
  fillSelect('conditionFilter', [...new Set(trialDatabase.map(t => t.condition))]);
  fillSelect('interventionFilter', [...new Set(trialDatabase.map(t => t.intervention))]);
  fillSelect('topicFilter', [...new Set(trialDatabase.map(t => t.guidelineTopic))]);
  el('categoryNav').innerHTML = ['all', ...categoryOrder].map(c => `<button data-cat="${escapeAttr(c)}" class="${c === activeCategory ? 'active' : ''}">${c === 'all' ? 'All' : escapeHtml(c)}</button>`).join('');
  el('categoryNav').querySelectorAll('button').forEach(btn => btn.addEventListener('click', () => {
    activeCategory = btn.dataset.cat;
    el('categoryFilter').value = activeCategory;
    populateFilters();
    renderTrials();
  }));
}
function getFilteredTrials() {
  const q = normalize(el('searchInput').value);
  const cat = el('categoryFilter').value;
  const cond = el('conditionFilter').value;
  const interv = el('interventionFilter').value;
  const topic = el('topicFilter').value;
  activeCategory = cat;
  return trialDatabase.filter(t => {
    const hay = normalize(Object.values(t).flat().join(' '));
    return (cat === 'all' || t.category === cat) && (cond === 'all' || t.condition === cond) && (interv === 'all' || t.intervention === interv) && (topic === 'all' || t.guidelineTopic === topic) && (!q || hay.includes(q));
  });
}
function renderTrials() {
  const items = getFilteredTrials();
  const grouped = new Map();
  categoryOrder.forEach(c => grouped.set(c, []));
  items.forEach(t => { if (!grouped.has(t.category)) grouped.set(t.category, []); grouped.get(t.category).push(t); });
  let html = '';
  grouped.forEach((trials, cat) => {
    if (!trials.length) return;
    html += `<div class="category-heading" id="${slug(cat)}"><h3>${escapeHtml(cat)}</h3><small>${trials.length} trial${trials.length > 1 ? 's' : ''}</small></div><div class="trial-grid">`;
    html += trials.map(renderCard).join('');
    html += `</div>`;
  });
  el('trialContainer').innerHTML = html || `<div class="card"><strong>No trials match your filters.</strong></div>`;
  el('trialContainer').querySelectorAll('.trial-summary').forEach(btn => btn.addEventListener('click', () => btn.closest('.trial-card').classList.toggle('open')));
  populateFilters();
  renderReferences(items);
}
function renderCard(t) {
  const s = statusFor(t);
  const statusClass = s.includes('found') ? 'found' : s.includes('not detected') ? 'missing' : 'database';
  const details = [
    ['Clinical question', t.clinicalQuestion], ['Guideline section where used', t.guidelineTopic], ['Population / inclusion criteria', t.population], ['Intervention', t.intervention], ['Comparator', t.comparator], ['Sample size', t.sampleSize], ['Primary outcome', t.primaryOutcome], ['Main result', t.mainResult], ['Practice-changing conclusion', t.practiceChangingConclusion], ['Rationale', t.rationale], ['Criticism', t.criticism], ['Limitations', t.limitations], ['How it changed clinical practice', t.changedPractice], ['Key reference', t.reference]
  ];
  return `<article class="trial-card">
    <button class="trial-summary" aria-expanded="false">
      <div class="trial-title-row"><div><h4>${escapeHtml(t.name)}</h4><div class="badges"><span class="badge year">${escapeHtml(t.year)}</span><span class="badge">${escapeHtml(t.condition)}</span><span class="badge ${statusClass}">${escapeHtml(s)}</span></div></div><span aria-hidden="true">⌄</span></div>
      <p>${escapeHtml(t.practiceChangingConclusion)}</p>
    </button>
    <div class="trial-details"><div class="detail-grid">${details.map(([k,v]) => `<div class="detail"><strong>${escapeHtml(k)}</strong><span>${escapeHtml(v)}</span></div>`).join('')}</div></div>
  </article>`;
}
function renderReferences(items = trialDatabase) {
  const unique = uniqueByName(items);
  el('referenceList').innerHTML = unique.map(t => `<div class="ref-item"><strong>${escapeHtml(t.name)}.</strong> ${escapeHtml(t.reference)} <span class="muted">[${escapeHtml(t.category)}]</span></div>`).join('');
}
function exportRows() {
  return getFilteredTrials().map(t => ({
    status: statusFor(t), trialName: t.name, year: t.year, clinicalQuestion: t.clinicalQuestion, strokeCondition: t.condition, guidelineSection: t.guidelineTopic, population: t.population, intervention: t.intervention, comparator: t.comparator, sampleSize: t.sampleSize, primaryOutcome: t.primaryOutcome, mainResult: t.mainResult, practiceChangingConclusion: t.practiceChangingConclusion, rationale: t.rationale, criticism: t.criticism, limitations: t.limitations, changedPractice: t.changedPractice, keyReference: t.reference, category: t.category
  }));
}
function toCsv(rows) {
  if (!rows.length) return '';
  const headers = Object.keys(rows[0]);
  return [headers.join(','), ...rows.map(r => headers.map(h => `"${String(r[h] ?? '').replace(/"/g, '""')}"`).join(','))].join('\n');
}
function downloadCsv() {
  const csv = toCsv(exportRows());
  const blob = new Blob([csv], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = 'stroke_landmark_trials.csv'; a.click(); URL.revokeObjectURL(url);
}
